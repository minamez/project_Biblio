package ProjetEcole1.ProjectBiblio;

public class Livre {
	private int refLivre;
	private String titreLivre;
	private int anneeLivre;
	private String editeurLivre;
	private String nomAuteur;
	private String prenomAuteur;

	public int getRefLivre() {
		return refLivre;
	}

	public void setRefLivre(int refLivre) {
		this.refLivre = refLivre;
	}

	public String getTitreLivre() {
		return titreLivre;
	}

	public void setTitreLivre(String titreLivre) {
		this.titreLivre = titreLivre;
	}

	public int getAnneeLivre() {
		return anneeLivre;
	}

	public void setAnneeLivre(int anneeLivre) {
		this.anneeLivre = anneeLivre;
	}

	public String getEditeurLivre() {
		return editeurLivre;
	}

	public void setEditeurLivre(String editeurLivre) {
		this.editeurLivre = editeurLivre;
	}

	public String getNomAuteur() {
		return nomAuteur;
	}

	public void setNomAuteur(String nomAuteur) {
		this.nomAuteur = nomAuteur;
	}

	public String getPrenomAuteur() {
		return prenomAuteur;
	}

	public void setPrenomAuteur(String prenomAuteur) {
		this.prenomAuteur = prenomAuteur;
	}

	public Livre() {

	}

}
